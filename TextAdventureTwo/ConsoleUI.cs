        static string[] MessagesSection()
        {

            var messages = UIStrings["StatsConstantsAndMessages"].Concat(UIStrings["ConstantOptions"]).ToArray();

            return messages;


        }


        static string[] NameAndCurrent(string name, string current)
        {
            var edited = UIStrings["NameAndCurrent"];
            var fields = new Regex(@".*( #+ ).*( &+ ).*").Match(edited[4]).Groups.Values.Select(x => x.ToString()).ToArray();
            var nameField = name.PadLeft(fields[1].Length / 2 + name.Length / 2).PadRight(fields[1].Length);
            var currentField = current.PadLeft(fields[2].Length/2 + current.Length/2).PadRight(fields[2].Length);
            var newLine = fields[0].Replace(fields[1], nameField).Replace(fields[2], currentField);
            edited[4] = edited[4].Replace(fields[0], newLine);
            return edited;
        }

